  'The International Atomic Energy Agency (IAEA) has developed a structured framework to help countries safely and systematically develop their first nuclear power program. Known as the Milestones Approach, it breaks the development journey into three distinct phases, each ending with a milestone that must be achieved before the next phase can begin.

Understanding this framework is essential for anyone working in or studying nuclear newcomer programs — from government officials and engineers to consultants, investors, and journalists covering the sector.

## The Three Milestones

The Milestones Approach is defined in IAEA Nuclear Energy Series No. NG-G-3.1, commonly called SSG-16. It establishes three milestones:

**Milestone 1 — Ready to make a knowledgeable commitment to a nuclear power programme**

At this stage, the country has conducted sufficient feasibility studies to make an informed decision about whether to pursue nuclear power. This includes assessing energy needs, evaluating the economics of nuclear versus alternatives, understanding the long-term commitments required, and securing political support.

Achieving Milestone 1 means the country is ready to commit to developing the infrastructure nuclear power requires — not yet to build a plant.

**Milestone 2 — Ready to invite bids for its first nuclear power plant**

The country has developed the national infrastructure to a level where it can responsibly contract with a technology vendor and begin construction. Regulatory and legal frameworks are in place, a site has been selected and characterized, financing plans are developed, and the workforce pipeline has been established.

Milestone 2 is the gate that separates "planning" from "doing."

**Milestone 3 — Ready to commission and operate its first nuclear power plant**

All infrastructure is complete. The regulatory body can independently license and oversee the plant. The operator has trained personnel ready to run the facility. The nuclear fuel supply chain, waste management arrangements, and emergency preparedness systems are in place.

Passing Milestone 3 means the country is ready to load fuel and begin generating electricity.

## The 19 Infrastructure Issues

The Milestones Approach assesses progress across 19 specific infrastructure issues, each representing a critical area that every nuclear newcomer country must address. These issues are grouped into three broad areas: national commitments, nuclear safety, and nuclear power infrastructure.

