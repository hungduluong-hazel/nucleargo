## The INIR Mission Process

Countries invite the IAEA to conduct INIR missions at each phase transition. A team of international nuclear experts reviews progress across all 19 infrastructure issues, conducts interviews with government and regulatory officials, and produces a detailed report with recommendations.

INIR missions are not pass/fail examinations — they are peer reviews designed to identify gaps and guide corrective action. Countries are not "approved" by the IAEA; they use the INIR process to benchmark their own readiness.

Vietnam completed its Phase 2 INIR mission in December 2025, confirming major progress across all 19 issues ahead of moving toward construction planning.

## How Long Does Each Phase Take?

Based on the experience of recent nuclear newcomer countries, the Milestones Approach typically requires:

- **Phase 1** (from decision to Milestone 1): 3-5 years
- **Phase 2** (from Milestone 1 to Milestone 2): 5-8 years
- **Phase 3** (from Milestone 2 to Milestone 3, including construction): 8-12 years

Total timeline from initial decision to first power: typically 15-25 years for a well-resourced program.

The UAE''s Barakah program is frequently cited as best-in-class: the country made its nuclear decision in 2008, selected Westinghouse/KEPCO in 2009, and achieved first power in 2021 — 13 years from decision to generation. Vietnam is targeting a significantly faster timeline, which makes its workforce development and regulatory capacity challenges especially acute.

## Why the Framework Matters

The Milestones Approach exists because nuclear power is not simply a technology purchase — it is a generational commitment that requires deep institutional, legal, and technical infrastructure. Countries that have attempted to develop nuclear programs without addressing these 19 issues systematically have encountered serious setbacks.

